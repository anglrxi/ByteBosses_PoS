    public void table_load() {
    try (Connection con = DatabaseConnection.getConnection()) {
        // This selects all your products to show in the table
        String sql = "SELECT * FROM products"; 
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0); // This clears the table so it doesn't double the data
        
        while(rs.next()) {
            // This grabs each row from your database and puts it into the JTable
            Object[] row = {
                rs.getString("pid"),
                rs.getString("product_name"),
                rs.getString("bar_code"),
                rs.getString("price"),
                rs.getString("qty_on_hand")
            };
            dt.addRow(row);
        }
    } catch (Exception e) {
        // If there is a connection error, it will print here
        System.out.println(e);
    }
}

