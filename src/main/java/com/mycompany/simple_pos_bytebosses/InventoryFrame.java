    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
                                               
    // 1. Get the data from your text fields
    String name = txt_name.getText();
    String barcode = txt_barcode.getText();
    String price = txt_price.getText();
    String stock = txt_stock.getText();
    
    // 2. Get the ID of the product from the selected row in the table
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row from the table first!");
        return;
    }
    String id = jTable1.getValueAt(row, 0).toString(); 

    try (Connection con = DatabaseConnection.getConnection()) {
    // 3. Prepare the SQL Update command - ADDED qty_on_hand=? here
    String sql = "UPDATE products SET product_name=?, bar_code=?, price=?, qty_on_hand=? WHERE pid=?";
    PreparedStatement pst = con.prepareStatement(sql);

    pst.setString(1, name);
    pst.setString(2, barcode);
    pst.setString(3, price);
    pst.setString(4, stock); // This is now the 4th ?
    pst.setString(5, id);    // This is now the 5th ? (the WHERE clause)

    // 4. Execute and Refresh
    pst.executeUpdate();
    JOptionPane.showMessageDialog(this, "Product Updated successfully!");

    table_load(); 

    } catch (Exception e) {
        System.out.println("Update Error: " + e.getMessage());
        JOptionPane.showMessageDialog(this, "Update Failed: " + e.getMessage());
    }

    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // 1. Get the ID from the selected row
    int row = jTable1.getSelectedRow();
    
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a product from the table to delete.");
        return;
    }
    
    String id = jTable1.getValueAt(row, 0).toString();
    String name = jTable1.getValueAt(row, 1).toString();

    // 2. Ask for confirmation (Safety first!)
    int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete " + name + "?", "Warning", JOptionPane.YES_NO_OPTION);
    
    if (dialogResult == JOptionPane.YES_OPTION) {
        try (Connection con = DatabaseConnection.getConnection()) {
            // 3. The SQL Delete command
            String sql = "DELETE FROM products WHERE pid = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, id);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Deleted Successfully");
            
            // 4. Refresh the table
            table_load();
            
            // 5. Clear the text fields
            txt_name.setText("");
            txt_barcode.setText("");
            txt_price.setText("");
            
        } catch (Exception e) {
            System.out.println("Delete Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btn_deleteActionPerformed

